package PRIMIERO_BIMESTRE.aula7.trabalho_proxy;

public class Livro {
    private String autor;
    private String titulo;
    private int isbn;

    public Livro(String autor, String titulo, int isbn) {
        this.autor = autor;
        this.titulo = titulo;
        this.isbn = isbn;
    }

    public Livro() {
        this.autor = autor;
        this.titulo = titulo;
        this.isbn = isbn;
    }


}
